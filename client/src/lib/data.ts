// Element categories for color coding
export type ElementCategory = 
  | 'alkali' 
  | 'alkaline' 
  | 'transition' 
  | 'posttransition' 
  | 'metalloid' 
  | 'nonmetal' 
  | 'halogen' 
  | 'noblegas' 
  | 'lanthanide' 
  | 'actinide';

// Physical state
export type ElementState = 'solid' | 'liquid' | 'gas' | 'unknown';

// Element interface
export interface Element {
  id: number;
  name: string;
  symbol: string;
  category: ElementCategory;
  atomic_mass: number;
  period: number;
  group: number | string; // Can be a number or "lanthanide"/"actinide"
  state: ElementState;
  electronConfig: string;
  electronegativity?: number; // Optional because some elements don't have this
  density?: number;
  meltingPoint?: number;
  boilingPoint?: number;
  ionizationEnergy?: number;
  description: string;
}

