// Get grid positions for the periodic table
export function getElementPosition(element: Element): { column: number; row: number } {
  // Handle special cases for lanthanides and actinides
  if (element.period === 6 && typeof element.group === 'number' && element.group >= 3 && element.id >= 58 && element.id <= 71) {
    return { column: element.id - 54, row: 8 }; // Lanthanides row
  }
  
  if (element.period === 7 && typeof element.group === 'number' && element.group >= 3 && element.id >= 90 && element.id <= 103) {
    return { column: element.id - 86, row: 9 }; // Actinides row
  }
  
  // Standard elements
  const column = typeof element.group === 'number' ? element.group : 3;
  const row = element.period;
  
  return { column, row };
}
